public class Escape {
    public static void main(String[] args){
        System.out.println("hello \'Rajnish\'");
        System.out.println("hello \"Rajnish\"");
        System.out.println("hello \n Rajnish");
        System.out.println("hello\tRajnish");
        System.out.println("hellow\b Rajnish");
        System.out.println("*\n* *\n* * *\n* * * *\n* * * * *");
        System.out.println("* * * * *\n* * * *\n* * *\n* *\n*");
        System.out.println("* * * * *\n* * * *\n* * *\n* *\n*");
    }
}
